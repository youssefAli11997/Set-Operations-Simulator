    private void addElementsSetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addElementsSetActionPerformed
        if(setNumber.getText().equals(""))
            return;
        int enteredSetNumber = Integer.parseInt(setNumber.getText());
        maxEnteredSetNum = Math.max(maxEnteredSetNum, enteredSetNumber);
        String[] setArr = setAdd.getText().split("\\s*,\\s*");
        for(String ele : setArr){
            boolean dup = false;
            boolean inUniv = false;
            for(int j=0; j<sets.get(enteredSetNumber).size(); j++){
                if(ele.equals(sets.get(enteredSetNumber).get(j))){
                    dup = true;
                    break;
                }
            }
            for(int i=0; i<universe.size(); i++){
                System.out.println(universe.get(i) + " " + ele);
                if(universe.get(i).equals(ele)){
                    inUniv = true;
                    break;
                }
            }
            System.out.println(inUniv);
            if(!dup && inUniv){
                System.out.println(ele);
                sets.get(enteredSetNumber).add(ele);
            }
        }
        System.out.println(sets);
        myProg = new SetOfStrings(universe, maxSetsNumber, sets);
    }//GEN-LAST:event_addElementsSetActionPerformed

