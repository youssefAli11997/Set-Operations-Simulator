    private void showSetBntActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showSetBntActionPerformed
        if(setNumberShow.getText().equals(""))
            return;
        if(setNumberShow.getText().equalsIgnoreCase("all") || setNumberShow.getText().equalsIgnoreCase("\"all\"")){
            // print all sets
            int cnt = -1;
            StringBuilder sb = new StringBuilder();
            for(ArrayList<String> set : myProg.getSets()){
                cnt++;
                if(set.isEmpty())
                    continue;
                sb.append("Set #" + cnt + " : ");
                sb.append("{ ");
                for(int i=0; i<set.size(); i++){
                    sb.append(set.get(i));
                    if(i<set.size()-1)
                        sb.append(", ");
                }
                sb.append(" }\n");
                if(cnt == maxEnteredSetNum)
                    break;
            }
            showSetText.setText(sb.toString());
            return;
        }
        int enteredSetNumber = Integer.parseInt(setNumberShow.getText());
        StringBuilder sb = new StringBuilder();
        sb.append("{ ");
        for(int i=0; i<myProg.getSets().get(enteredSetNumber).size(); i++){
            sb.append(myProg.getSets().get(enteredSetNumber).get(i));
            if(i<myProg.getSets().get(enteredSetNumber).size()-1)
                sb.append(", ");
        }
        sb.append(" }");
        showSetText.setText(sb.toString());
    }//GEN-LAST:event_showSetBntActionPerformed

